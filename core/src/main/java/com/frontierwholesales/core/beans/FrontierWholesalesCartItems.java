package com.frontierwholesales.core.beans;

public class FrontierWholesalesCartItems {

	private String items_qty;
	private String tax_amount;
	private String grand_total;
	private String discount_amount;
	private String base_grand_total;
	public String getItems_qty() {
		return items_qty;
	}
	public void setItems_qty(String items_qty) {
		this.items_qty = items_qty;
	}
	public String getTax_amount() {
		return tax_amount;
	}
	public void setTax_amount(String tax_amount) {
		this.tax_amount = tax_amount;
	}
	public String getGrand_total() {
		return grand_total;
	}
	public void setGrand_total(String grand_total) {
		this.grand_total = grand_total;
	}
	public String getDiscount_amount() {
		return discount_amount;
	}
	public void setDiscount_amount(String discount_amount) {
		this.discount_amount = discount_amount;
	}
	public String getBase_grand_total() {
		return base_grand_total;
	}
	public void setBase_grand_total(String base_grand_total) {
		this.base_grand_total = base_grand_total;
	}
	
	
}
